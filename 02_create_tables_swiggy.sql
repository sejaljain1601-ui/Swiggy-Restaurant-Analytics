CREATE TABLE Restaurant_Data (
	 Restaurant_Id INT PRIMARY KEY,
     Area VARCHAR(50),
     City VARCHAR(25),
     Restaurant_Name VARCHAR(75),
     Average_Price_INR INT,
     Avg_Ratings DECIMAL(3,1),
     Total_Ratings INT,
     Food_Type VARCHAR(200),
     Address TEXT,
     Delivery_Time_Minutes INT
     );
     
     
     
     

     
     


     



     
     
